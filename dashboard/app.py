@st.cache_data(ttl=300)
def read_delta_table(path):

    try:

        parquet_files = []

        for root, dirs, files in os.walk(path):
            for file in files:
                if file.endswith(".parquet"):
                    parquet_files.append(os.path.join(root, file))

        if parquet_files:
            df = pd.concat([pd.read_parquet(f) for f in parquet_files])
            return df

        return pd.DataFrame()

    except Exception:
        return pd.DataFrame()


def get_table_info(path):

    if os.path.exists(path):

        df = read_delta_table(path)

        if not df.empty:
            return df

    return pd.DataFrame()


def get_layer_metrics(layer_name):
    """Reads the JSON metrics file generated by Spark jobs."""
    try:
        path = os.path.join(DATA_DIR, "delta", "metrics", f"{layer_name.lower()}_metrics.json")
        if os.path.exists(path):
            with open(path, "r") as f:
                return json.load(f)
    except Exception:
        return {}
    return {}


# ---------------------------------------------------
# Trigger Airflow DAG
# ---------------------------------------------------

def trigger_airflow(file_name: str):

    try:

        response = requests.post(
            f"{AIRFLOW_URL}/api/v1/dags/{AIRFLOW_DAG_ID}/dagRuns",
            auth=(AIRFLOW_USERNAME, AIRFLOW_PASSWORD),
            headers={"Content-Type": "application/json"},
            json={"conf": {"file_name": file_name}},
            timeout=10
        )

        if response.status_code in [200, 201]:

            return True

        else:

            st.error(f"Airflow API error {response.status_code}")
            st.code(response.text)
            return False

    except Exception as e:

        st.error(f"Airflow trigger failed: {str(e)}")
        return False


def stop_pipeline(run_id):
    """Stops a running pipeline by marking it as failed."""
    try:
        response = requests.patch(
            f"{AIRFLOW_URL}/api/v1/dags/{AIRFLOW_DAG_ID}/dagRuns/{run_id}",
            auth=(AIRFLOW_USERNAME, AIRFLOW_PASSWORD),
            headers={"Content-Type": "application/json"},
            json={"state": "failed"},
            timeout=10
        )

        if response.status_code == 200:
            return True
        else:
            st.error(f"Failed to stop pipeline: {response.text}")
            return False

    except Exception as e:
        st.error(f"Error stopping pipeline: {str(e)}")
        return False


# ---------------------------------------------------
# Get Pipeline Runs
# ---------------------------------------------------

def get_dag_runs():

    try:

        response = requests.get(
            f"{AIRFLOW_URL}/api/v1/dags/{AIRFLOW_DAG_ID}/dagRuns?limit=5",
            auth=(AIRFLOW_USERNAME, AIRFLOW_PASSWORD),
            headers={"Content-Type": "application/json"},
            timeout=10
        )

        if response.status_code == 200:

            return response.json().get("dag_runs", [])

        else:

            st.error(f"Airflow API error {response.status_code}")
            return []

    except Exception:

        st.error("Unable to fetch pipeline status")
        return []


