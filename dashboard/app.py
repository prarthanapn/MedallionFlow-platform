        # Only proceed if there are valid files to process.
        if valid_files:
            st.success(f"✅ {len(valid_files)} CSV file(s) are valid and ready to upload.")

            # Check for running pipelines to avoid conflicts
            running_runs = [r for r in get_dag_runs() if r['state'] == 'running']

            if running_runs:
                st.warning(f"⚠️ A pipeline is already running ({running_runs[0]['dag_run_id']}). Please wait for it to finish.")
            elif st.button("🚀 Upload & Start Pipeline"):

                os.makedirs(INPUT_PATH, exist_ok=True)

                # Only save the valid files
                for file in valid_files:
                    path = os.path.join(INPUT_PATH, file.name)
                    with open(path, "wb") as f:
                        f.write(file.getbuffer())

                st.success("Valid CSV files uploaded successfully.")

                # We'll trigger the pipeline for the first valid uploaded file.
                file_to_process = valid_files[0].name
                st.info(f"Triggering pipeline for: {file_to_process}...")

                if trigger_airflow(file_to_process):

                    st.success("Pipeline started successfully")
                    st.balloons()
                    st.link_button(
                        "Open Airflow Dashboard",
                        "http://localhost:8081"
                    )

                else:
                    st.error("Failed to trigger DAG")

